part of "sizer.dart";

/// Enum to represent the type of device.
enum DeviceType {
  /// Represents a mobile device, such as a smartphone.
  mobile,

  /// Represents a tablet device.
  tablet,

  /// Represents a web platform, including web browsers.
  web,

  /// Represents a Mac desktop or laptop computer.
  mac,

  /// Represents a Windows desktop or laptop computer.
  windows,

  /// Represents a Linux desktop or laptop computer.
  linux,

  /// Represents a device running the Fuchsia operating system.
  fuchsia,
}
